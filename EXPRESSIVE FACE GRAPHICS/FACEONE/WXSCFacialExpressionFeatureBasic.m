#import "WXSCFacialExpressionFeatureBasic.h"


@implementation WXSCFacialExpressionFeatureBasic

// ******************************************************************************************
-(id)		initAt:(float)x y:(float)y
{
if(self=[super init])
	{
	xpos=x; ypos=y;
	moodType=0;
	mouseIsDown=NO;
	}
return self;
}

// ************************************************************************************************************
-(NSImage*) doLoadImage:(NSString*)imageName
{
NSImage* image;
image=[NSImage imageNamed:imageName];
[image	retain];
[image	lockFocus];
[image	unlockFocus];
return image;
}
// ******************************************************************************************
