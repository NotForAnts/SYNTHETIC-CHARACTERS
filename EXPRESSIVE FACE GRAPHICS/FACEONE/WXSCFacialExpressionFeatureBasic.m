{
}
// ******************************************************************************************
-(void) mouseDown:(NSEvent*)theEvent	view:(NSView*)view
{
mouseIsDown=YES;
}
// ******************************************************************************************
-(void) mouseUp:(NSEvent*)theEvent	view:(NSView*)view
{
mouseIsDown=NO;
}
